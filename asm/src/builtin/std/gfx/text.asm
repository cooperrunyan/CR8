#[macro]
draw_char: {
    ($to: expr, $char: expr) => {
        mov %a, %b, $char
        mov %c, %d, $to
        call _draw_char
    }
}

_draw_char:
    ; row 0
    ldxy %a, %b
    lw %f
    ldxy %c, %d
    sw %f

    ; row 1
    inc %a, %b
    add %c, %d, 32, 0
    ldxy %a, %b
    lw %f
    ldxy %c, %d
    sw %f

    ; row 2
    inc %a, %b
    add %c, %d, 32, 0
    ldxy %a, %b
    lw %f
    ldxy %c, %d
    sw %f

    inc %a, %b
    add %c, %d, 32, 0
    ldxy %a, %b
    lw %f
    ldxy %c, %d
    sw %f

    inc %a, %b
    add %c, %d, 32, 0
    ldxy %a, %b
    lw %f
    ldxy %c, %d
    sw %f

    inc %a, %b
    add %c, %d, 32, 0
    ldxy %a, %b
    lw %f
    ldxy %c, %d
    sw %f

    inc %a, %b
    add %c, %d, 32, 0
    ldxy %a, %b
    lw %f
    ldxy %c, %d
    sw %f

    inc %a, %b
    add %c, %d, 32, 0
    ldxy %a, %b
    lw %f
    ldxy %c, %d
    sw %f

    ret

